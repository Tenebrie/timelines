import { useTheme } from '@mui/material'
import Stack from '@mui/material/Stack'
import Switch from '@mui/material/Switch'
import { useCallback } from 'react'
import { useDispatch, useSelector } from 'react-redux'
import styled from 'styled-components'

import { preferencesSlice } from '@/app/features/preferences/reducer'
import { getWikiPreferences } from '@/app/features/preferences/selectors'

export const ReadModeToggle = () => {
	const { readModeEnabled } = useSelector(getWikiPreferences)

	const { setReadMode } = preferencesSlice.actions
	const dispatch = useDispatch()

	const onChange = useCallback(
		(checked: boolean) => {
			dispatch(setReadMode(!checked))
		},
		[dispatch, setReadMode],
	)

	const theme = useTheme()

	return (
		<Stack marginTop={0.25}>
			<MaterialUISwitch
				aria-label="Toggle read/edit mode"
				theme={theme}
				checked={!readModeEnabled}
				onChange={(event) => onChange(event.target.checked)}
			></MaterialUISwitch>
		</Stack>
	)
}

